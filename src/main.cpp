#include <vector>
using namespace std;

void deleteMatrix(int **m, int n);
void deleteMatrix(double **m, int n);
inline void deleteMatrix(int **m, int n) {
    if (m != nullptr) {
        for (int i = 0; i < n; i++)
            if (m[i] != nullptr)
                delete [] m[i];
        delete [] m;
    }
}

